    <p>As much or as little as you like. The project is designed to be flexible because we realise everybody has different commitments. We recommend setting aside half an hour at the same time each morning when you wake up. But if you have a really busy day, don’t stress – just save the email and come back to it later.</p>

    <h3>4. What is an ELC ambassador?</h3>

    <p>An ELC Ambassador is someone who engages and inspires people in their local area to take part in the Ethical Living Challenge and join the ethical living movement. This involves encouraging and talking to people who are also doing the challenge, and also engaging the wider community – talking to local papers and other media outlets, contacting schools and universities, writing blog posts and trying to spread the world about the challenge and about ethical living. Ambassadors will be guided through the process and provided with resources along the way. Positions are open to anybody around the world of any age.</p>

    <h3>5. How do I become an ELC ambassador?</h3>

    <p>Visit our <a href="connect">Connect</a> page and follow the ‘Apply to be an Ambassador’ link. After that, you’ll have to answer a few questions and then we’ll get in touch regarding the outcome of your application. Make sure you’ve signed up to the actual challenge, too!</p>

    <h3>6. Do I get anything in return?</h3>

    <p>Firstly, you receive that warm fuzzy feeling knowing that you’re living a bit (or a lot) more ethically than before. Secondly there will be occasional competitions and giveaways for our participants, sponsored by some amazing ethical companies. So if you’re lucky, you could find yourself owning a new organic t-shirt or Fairtrade hot chocolate. Thirdly, you’ll receive a leadership action pack and loads of resources which you’ll have for life. Finally, if you’re selected as an ELC Ambassador then you can pop it on your CV when you apply for jobs or other roles.</p>

