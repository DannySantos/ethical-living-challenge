    <h2>The Challenge</h2>  

    <p>The Ethical Living Challenge (ELC) is about learning how to live more ethically in the world by treading lightly and causing the least harm possible. This means revealing the truth behind what we purchase, understanding how what we do today affects how we live tomorrow, and taking action on global issues.</p>

    <p>Our purchases and actions often have horrific effects around the world - they can fuel slavery, keep people in poverty, deny workers their rights, and destroy the environment.</p>

    <p>Ethical living is about ensuring your day-to-day decisions don't contribute to these things. ELC aims to create a generation of ethical leaders who are aware of the issues and know how to make the right choices. It's not necessarily about radically changing your habits - it's about knowing how to make change step by step. Read on for more information.</p>
    
    <h2>How it works</h2> 

    <p>When you sign up below, a daily e-mail will be sent to you for the next 21 days. It will have a different theme each day - everything from the ethics of chocolate to the fashion industry to overseas travel. You'll learn a bit about the topic, then you'll be given an optional daily challenge which might be writing a letter to a politician, calculating your slavery footprint or talking to your favourite cafe about Fair Trade.</p> 

    <p>Each e-mail will give you the tools and knowledge for you to start - or continue - your ethical living journey and create change after the challenge finishes. It's up to you how much time you dedicate to the challenge and how you'll make a difference. We recommend saving the e-mails to come back to at a later stage, too. Be sure to read our FAQs page and get in touch if you have any questions or would like to get more involved.</p>

